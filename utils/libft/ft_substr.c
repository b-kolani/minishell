#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len, int f)
{
	size_t	i;
	size_t	s_len;
	char	*ptr;

	if (!s)
		return (NULL);
	s_len = ft_strlen(s);
	if (start >= s_len)
		return (ft_strdup("", f));
	if (len > s_len - start)
		len = s_len - start;
	ptr = check_collectors((len + 1), f);
	i = 0;
	while (i < len)
	{
		ptr[i] = s[start + i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
